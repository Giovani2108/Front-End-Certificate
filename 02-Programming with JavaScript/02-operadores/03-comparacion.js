let a = 10;

//Relacionales
console.log(a > 5);
console.log(a >= 5);

console.log(a < 5);
console.log(a <= 5);


//Igualdad
console.log(a == 10);
console.log(a!=10);
console.log(a == '10'); //Solo revisa el valor

console.log(a === '10'); // Revisa valor y tipo de dato
console.log( a !== '10'); // Pregunta si son diferentes tipos