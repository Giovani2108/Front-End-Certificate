// Parecido a sentencia If

let edad = 16;
//si se cumple la condicion, devolver primera, sino segunda
let acceso = edad > 17 ? 'Permitir ingreso' : 'No puede ingresar';
console.log(acceso);  // Resultado: "No puede ingresar"

edad = 20;
acceso = edad > 17 ? 'Permitir ingreso' : 'No puede ingresar';
console.log(acceso);  // Resultado: "No puede ingresar"